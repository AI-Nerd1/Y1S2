	z = Convert::ToDouble(textBox3->Text);
	
	if ((y> x+1)||(z> y+1)||(y< x-1)||(z< y-1))
	{
		textBox4-> Text += "\r\nЦифры не в последовательности \r\n";
	}
	else if ((x<y)&&(y<z))
	{
		textBox4-> Text += "\r\nПоследовательность Увеличивается \r\n";
	}
